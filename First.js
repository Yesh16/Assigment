// using let 
let a = 5;
let b = 6;
let sum = a + b;
console.log(`Sum using let: ${sum}`)

//using var 
var c = 7 
var d = 8 
var product = c * d
console.log(`Product using var: ${product}`);

//using const 
const x = 4;
const y = 6;
const division = x/y;
console.log(`Division using const: ${division}`);

//combine all 
let total =  sum +  product + division;
console.log(`Total combining all operations: ${total}`)